  parseArgv(command) {
    if (!command) {
      return
    }
    const commandArr = command.replace(/\s+/g, ' ').split(' ')
    const nameSpace = commandArr[0]
    const action = commandArr[1]
    let paramObj = minimist(commandArr)
    paramObj.action = action
    paramObj.nameSpace = nameSpace
    return paramObj
  },
